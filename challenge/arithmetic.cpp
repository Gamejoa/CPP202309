#include <iostream>

using namespace std;

int main() {

	int one = 1; // 정수형 변수 one 에는 1이라는 정수값이 저장되어 있음 
	int two = 2; // 이하 동일함 
	int three = 3;
	int four = 4;
	



	cout << "1 + 3 = " << one + three << endl; // 따라서, 1 + 3 값이 출력
	cout << "4 - 2 = " << four - two << endl; // 4 - 2 값이 출력
	cout << "2 * 3 = " << two * three << endl; // 이하 동일 
	cout << "4 / 2 = " << four / two << endl;

	cout << "2 + 3 * 4 = " << two + three * four << endl;
	cout << "2 + 3 * 4 = " << two + three * four << endl;
	cout << "1 + 4 / 2 = " << one + four / two << endl;
	


	return 0;


}